package com.flume2d.math;

public class Rectangle
{
	public float x;
	public float y;
	public float width;
	public float height;
	
	public Rectangle()
	{
		this(0, 0, 0, 0);
	}
	
	public Rectangle(float x, float y, float width, float height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
	
}
